EPOCHS_CV = 20
BATCH_CV  = 256

def _train_eval(ModelCls, X_tr, y_tr, X_vl, y_vl):
    m    = ModelCls(num_classes=5).to(device)
    opt  = torch.optim.AdamW(m.parameters(), lr=5e-4, weight_decay=1e-3)
    crit = nn.CrossEntropyLoss(label_smoothing=0.1)
    def _lr(ep): return (ep+1)/2 if ep < 2 else 0.5*(1+np.cos(np.pi*(ep-2)/(EPOCHS_CV-2)))
    sch  = torch.optim.lr_scheduler.LambdaLR(opt, _lr)
    ld   = DataLoader(TensorDataset(
               torch.tensor(X_tr, dtype=torch.float32),
               torch.tensor(y_tr, dtype=torch.long)),
               batch_size=BATCH_CV, shuffle=True, drop_last=True)
    for ep in range(EPOCHS_CV):
        m.train()
        for xb, yb in ld:
            xb, yb = xb.to(device), yb.to(device)
            opt.zero_grad(); crit(m(xb), yb).backward(); opt.step()
        sch.step()
    m.eval()
    vl_ld = DataLoader(TensorDataset(
                torch.tensor(X_vl, dtype=torch.float32),
                torch.tensor(y_vl, dtype=torch.long)),
                batch_size=512, shuffle=False)
    preds, trues = [], []
    with torch.no_grad():
        for xb, yb in vl_ld:
            preds.extend(m(xb.to(device)).argmax(1).cpu().numpy())
            trues.extend(yb.numpy())
    return f1_score(trues, preds, average='macro'), np.array(preds), m

def _get_preds(m, X):
    m.eval()
    preds = []
    with torch.no_grad():
        for s in range(0, len(X), 512):
            xb = torch.tensor(X[s:s+512], dtype=torch.float32).to(device)
            preds.extend(m(xb).argmax(1).cpu().numpy())
    return np.array(preds)

