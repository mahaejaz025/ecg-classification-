class _SEBlock(nn.Module):
    def __init__(self, ch, r=16):
        super().__init__()
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc   = nn.Sequential(
            nn.Linear(ch, max(ch // r, 4), bias=False), nn.ReLU(True),
            nn.Linear(max(ch // r, 4), ch, bias=False), nn.Sigmoid())
    def forward(self, x):
        b, c, _ = x.shape
        return x * self.fc(self.pool(x).view(b, c)).view(b, c, 1)

class _ResBlock(nn.Module):
    def __init__(self, ic, oc, stride=1):
        super().__init__()
        self.c1   = nn.Conv1d(ic, oc, 3, stride=stride, padding=1, bias=False)
        self.bn1  = nn.BatchNorm1d(oc)
        self.c2   = nn.Conv1d(oc, oc, 3, padding=1, bias=False)
        self.bn2  = nn.BatchNorm1d(oc)
        self.se   = _SEBlock(oc)
        self.skip = (nn.Sequential(nn.Conv1d(ic, oc, 1, stride=stride, bias=False),
                                    nn.BatchNorm1d(oc))
                     if ic != oc or stride != 1 else nn.Identity())
    def forward(self, x):
        out = F.relu(self.bn1(self.c1(x)))
        out = self.se(self.bn2(self.c2(out)))
        return F.relu(out + self.skip(x))

class ECGResNetSE_stat(nn.Module):
    """Baseline: ECGResNetSE for statistical comparison."""
    def __init__(self, num_classes=5, dropout=0.4):
        super().__init__()
        self.stem = nn.Sequential(
            nn.Conv1d(1, 32, 7, stride=2, padding=3, bias=False),
            nn.BatchNorm1d(32), nn.ReLU(True),
            nn.Conv1d(32, 32, 3, padding=1, bias=False),
            nn.BatchNorm1d(32), nn.ReLU(True))
        self.l1   = _ResBlock(32, 64, stride=2)
        self.l2   = _ResBlock(64, 128, stride=2)
        self.l3   = _ResBlock(128, 128)
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.drop = nn.Dropout(dropout)
        self.fc   = nn.Linear(128, num_classes)
    def forward(self, x):
        if x.dim() == 2: x = x.unsqueeze(1)
        return self.fc(self.drop(self.pool(
            self.l3(self.l2(self.l1(self.stem(x))))).squeeze(-1)))

class MultiScaleECGCNN_stat(nn.Module):
    """MultiScale CNN for CV (local clone if Cell 17 not in scope)."""
    def __init__(self, num_classes=5, dropout=0.5):
        super().__init__()
        ch = 48
        def _p(k):
            return nn.Sequential(
                nn.Conv1d(1, ch, k, padding=k//2, bias=False), nn.BatchNorm1d(ch), nn.ReLU(True),
                nn.Dropout(dropout * 0.5),
                nn.Conv1d(ch, ch, k, padding=k//2, bias=False), nn.BatchNorm1d(ch), nn.ReLU(True),
                nn.AdaptiveAvgPool1d(32))
        self.p1, self.p2, self.p3 = _p(3), _p(9), _p(21)
        mc = ch * 3
        self.se      = _SEBlock(mc, r=8)
        self.fusion  = nn.Sequential(
            nn.Dropout(dropout * 0.6),
            nn.Conv1d(mc, 96, 3, padding=1, bias=False), nn.BatchNorm1d(96), nn.ReLU(True),
            nn.AdaptiveAvgPool1d(1))
        self.clf     = nn.Sequential(
            nn.Dropout(dropout), nn.Linear(96, 64), nn.ReLU(True),
            nn.Dropout(dropout * 0.5), nn.Linear(64, num_classes))
    def forward(self, x):
        if x.dim() == 2: x = x.unsqueeze(1)
        f = torch.cat([self.p1(x), self.p2(x), self.p3(x)], 1)
        return self.clf(self.fusion(self.se(f)).squeeze(-1))

