def _make_pf(T):
    def _pf(x: np.ndarray) -> np.ndarray:
        t = torch.tensor(x.astype(np.float32)).to(device)
        with torch.no_grad():
            return F.softmax(_lime_model(t) / T, dim=1).cpu().numpy()
    return _pf

def _tune_temperature(X_class_samples, T_range,
                      target_min=0.65, target_max=0.90):
    samples = X_class_samples[:min(20, len(X_class_samples))]
    T_vals  = np.geomspace(T_range[0], min(T_range[1], T_MAX), 20)
    confs   = [float(_make_pf(T)(samples).max(axis=1).mean()) for T in T_vals]

    in_range = [(i, abs(c - target_min))
                for i, c in enumerate(confs)
                if target_min <= c <= target_max]
    if in_range:
        best_i = min(in_range, key=lambda v: v[1])[0]
    else:
        best_i = int(np.argmin([abs(c - target_min) for c in confs]))

    best_T = float(min(T_vals[best_i], T_MAX))
    return best_T, confs[best_i]

