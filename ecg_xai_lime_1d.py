def _t_ci(scores, confidence=0.95):
    if len(scores) < 2:
        m = float(np.mean(scores)); return (m, m)
    lo, hi = scipy_stats.t.interval(
        confidence, df=len(scores)-1,
        loc=np.mean(scores), scale=scipy_stats.sem(scores)
    )
    return float(lo), float(hi)

