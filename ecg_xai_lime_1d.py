def _dynamic_pwave_anchor(x: np.ndarray,
                           search_start=15,
                           search_end=120,
                           half_width=35,
                           min_amplitude_frac=0.05,
                           min_anchor_width=20):
    """
    Locate P-wave anchor dynamically for Supraventricular samples.
    Returns (lo, hi, method) where method is 'dynamic' or 'fallback'.
    """
    FALLBACK = (20, 110, "fallback")

    qrs_peak_t   = int(np.argmax(np.abs(x)))
    qrs_peak_amp = float(np.abs(x[qrs_peak_t]))

    pre_start = max(search_start, 0)
    pre_end   = min(qrs_peak_t, search_end)

    if pre_end <= pre_start + 5:
        return FALLBACK

    segment = x[pre_start:pre_end]

    candidates = []
    for i in range(1, len(segment) - 1):
        if (segment[i] > segment[i-1] and
                segment[i] > segment[i+1] and
                abs(segment[i]) > min_amplitude_frac * qrs_peak_amp):
            candidates.append((float(segment[i]), pre_start + i))

    if not candidates:
        return FALLBACK

    _, pwave_t = max(candidates, key=lambda v: v[0])
    lo = max(0, pwave_t - half_width)
    hi = min(INPUT_LEN - 1, pwave_t + half_width)

    if (hi - lo) < min_anchor_width:
        return FALLBACK

    return (int(lo), int(hi), "dynamic")


def _entropy_concentration(attr: np.ndarray) -> float:
    attr_norm   = np.abs(attr) / (np.abs(attr).sum() + 1e-8)
    entropy     = -np.sum(attr_norm * np.log(attr_norm + 1e-10))
    max_entropy = np.log(INPUT_LEN)
    return float(1.0 - (entropy / max_entropy))


def _run_lime_once(x, cls_idx):
    params    = CLASS_PARAMS[cls_idx]
    explainer = CLASS_EXPLAINERS[cls_idx]
    pf        = params["pf"]
    n_samples = params["n_samples"]

    exp = explainer.explain_instance(
        x.astype(np.float32),
        pf,
        num_features = INPUT_LEN,
        num_samples  = n_samples,
        top_labels   = 5
    )

    with torch.no_grad():
        raw_logits = _lime_model(
            torch.tensor(x[np.newaxis], dtype=torch.float32).to(device)
        )
    pc = int(raw_logits.argmax(dim=1).cpu().numpy()[0])

    if pc not in exp.local_exp:
        pc = list(exp.local_exp.keys())[0] if exp.local_exp else cls_idx

    if pc not in exp.local_exp:
        return np.zeros(INPUT_LEN), pc

    attr = np.zeros(INPUT_LEN)
    for feat_idx, weight in exp.local_exp[pc]:
        if 0 <= feat_idx < INPUT_LEN:
            attr[feat_idx] = weight
    return attr, pc


def _score_from_attr(attr, qrs):
    total = np.abs(attr).sum() + 1e-8
    return float(np.abs(attr[qrs[0]:qrs[1]]).sum() / total)


def _adaptive_trust(x: np.ndarray, cls_idx: int):
    """
    Compute LIME trust score. Runs LIME_RUNS times and averages attributions.
    Supraventricular: dynamic P-wave anchor with v5.5 guards.
    Unknown: entropy concentration penalty.
    """
    params = CLASS_PARAMS[cls_idx]
    qrs    = params["qrs"]
    diag   = {}

    if params.get("dynamic_anchor"):
        result = _dynamic_pwave_anchor(x)
        qrs    = (result[0], result[1])
        diag["dynamic_anchor"]        = qrs
        diag["dynamic_anchor_method"] = result[2]

    attrs = []
    pc    = cls_idx
    for _ in range(LIME_RUNS):
        attr_i, pc_i = _run_lime_once(x, cls_idx)
        attrs.append(attr_i)
        pc = pc_i

    attr  = np.mean(attrs, axis=0)
    score = _score_from_attr(attr, qrs)

    if cls_idx == 4:
        concentration         = _entropy_concentration(attr)
        diag["raw_score"]     = score
        diag["concentration"] = concentration
        score = max(score * concentration, 0.05)

    return score, attr, pc, {
        "T":         params["T"],
        "kw":        params["kw"],
        "n_samples": params["n_samples"],
        "qrs":       qrs,
    }, diag

