# ── Bootstrap Confidence Intervals (95%) ──────────────────────────────────────
def bootstrap_ci(y_true, y_pred, metric_fn, n_resamples=5000, ci=95, seed=42):
    """
    Compute bootstrap confidence interval for a metric.

    Args:
        y_true: ground truth labels
        y_pred: predicted labels
        metric_fn: function(y_true, y_pred) -> float
        n_resamples: number of bootstrap resamples
        ci: confidence interval (e.g., 95)
        seed: random seed

    Returns:
        (point_estimate, lower_ci, upper_ci)
    """
    rng = np.random.default_rng(seed)
    point_est = metric_fn(y_true, y_pred)

    bootstrap_scores = []
    n = len(y_true)

    for _ in range(n_resamples):
        idx = rng.choice(n, size=n, replace=True)
        score = metric_fn(y_true[idx], y_pred[idx])
        bootstrap_scores.append(score)

    bootstrap_scores = np.array(bootstrap_scores)
    alpha = (100 - ci) / 2
    lower = np.percentile(bootstrap_scores, alpha)
    upper = np.percentile(bootstrap_scores, 100 - alpha)

    return point_est, lower, upper, bootstrap_scores

