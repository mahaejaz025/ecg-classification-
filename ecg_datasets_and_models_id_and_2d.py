# ── Model B definition ───────────────────────────────────────────────────────
class MendeleyResNet(nn.Module):
    """
    2D CNN for ECG image classification (Mendeley dataset).
    Input: (N, 3, 224, 224) or (N, 224, 224, 3)
    Output: (N, 3)  — Normal / Abnormal / MI
    """
    def __init__(self, num_classes=3):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(3, 32, 3, padding=1), nn.BatchNorm2d(32), nn.ReLU(True), nn.MaxPool2d(2),
            nn.Conv2d(32, 64, 3, padding=1), nn.BatchNorm2d(64), nn.ReLU(True), nn.MaxPool2d(2),
            nn.Conv2d(64, 128, 3, padding=1), nn.BatchNorm2d(128), nn.ReLU(True), nn.MaxPool2d(2),
            nn.Conv2d(128, 256, 3, padding=1), nn.BatchNorm2d(256), nn.ReLU(True),
            nn.AdaptiveAvgPool2d((4, 4))
        )
        self.classifier = nn.Sequential(
            nn.Dropout(0.5),
            nn.Linear(256 * 4 * 4, 256),
            nn.ReLU(True),
            nn.Dropout(0.3),
            nn.Linear(256, num_classes)
        )

    def forward(self, x):
        if x.dim() == 4 and x.shape[-1] == 3:
            x = x.permute(0, 3, 1, 2)
        x = x.float()
        if x.max() > 1.0: x = x / 255.0
        return self.classifier(self.features(x).flatten(1))

# ── Mendeley Dataset ─────────────────────────────────────────────────────────
class MendeleyECGDataset(Dataset):
    def __init__(self, images, labels, augment=False):
        self.images  = torch.tensor(images, dtype=torch.float32)
        self.labels  = torch.tensor(labels, dtype=torch.long)
        self.augment = augment

    def __len__(self): return len(self.labels)

    def __getitem__(self, idx):
        img = self.images[idx]
        if self.augment:
            if torch.rand(1) > 0.5: img = img.flip(-1)      # horizontal flip
            img = img + 0.01 * torch.randn_like(img)         # small noise
            img = torch.clamp(img, 0, 1)
        return img, self.labels[idx]

