    dummy = torch.randn(batch_size, INPUT_LEN, dtype=torch.float32, device=device)

    with torch.no_grad():
        for _ in range(10):  # warmup
            _ = m(dummy)
    if device_name == "cuda":
        torch.cuda.synchronize()

    with torch.no_grad():
        if device_name == "cuda":
            torch.cuda.synchronize()
            t0 = time.perf_counter()
            for _ in range(n_batches):
                _ = m(dummy)
            torch.cuda.synchronize()
            t1 = time.perf_counter()
        else:
            t0 = time.perf_counter()
            for _ in range(n_batches):
                _ = m(dummy)
            t1 = time.perf_counter()

    total_samples = batch_size * n_batches
    elapsed = t1 - t0
    throughput = total_samples / elapsed
    return throughput

