# CELL NEW-D — ABLATION STUDY (CORRECTED)
# Isolates contribution of: SMOTE | SE blocks | Multi-scale paths | Augmentation
#
# Fixes vs. previous version:
#   1. "No SMOTE" branch previously trained on X_val[:5000] (a 5000-row slice
#      of the *validation* set) instead of a real imbalanced training pool of
#      comparable size to the other variants. That confounded "no SMOTE" with
#      "much smaller training set" and produced a near-3-second run collapsing
#      to F1=0.2552. Fixed below by bootstrap-resampling the real imbalanced
#      pool up to the SAME total N as the balanced pool, preserving natural
#      class proportions — so only the balancing mechanism differs, not the
#      amount of data or number of training steps.
#   2. Delta print had a hardcoded literal "-" in front of an already-signed
#      value, turning small positive deltas (component hurt performance) into
#      "--0.0131" instead of "+0.0131". Fixed with a proper signed format.
#   3. Single run per variant gave no way to tell architecture effect from
#      run-to-run noise. Fixed by running N_SEEDS repeats per variant and
#      reporting mean +/- std.
# =============================================================================
