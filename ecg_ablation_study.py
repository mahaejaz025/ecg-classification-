class NoAugDataset(Dataset):
    def __init__(self, X, y):
        self.X = torch.tensor(X, dtype=torch.float32)
        self.y = torch.tensor(y, dtype=torch.long)
    def __len__(self): return len(self.y)
    def __getitem__(self, i): return self.X[i], self.y[i]

class AugDataset(Dataset):
    def __init__(self, X, y):
        self.X = torch.tensor(X, dtype=torch.float32)
        self.y = torch.tensor(y, dtype=torch.long)
    def __len__(self): return len(self.y)
    def __getitem__(self, i):
        x = self.X[i] + 0.02*torch.randn_like(self.X[i])
        x = x * (0.8 + 0.4*torch.rand(1))
        return x, self.y[i]

EPOCHS_AB = 10
BATCH_AB  = 256

def set_seed(seed):
    torch.manual_seed(seed)
    np.random.seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

def train_eval(model_fn, X_tr, y_tr, X_vl, y_vl, aug=False, seed=0):
    set_seed(seed)
    tr_ds = AugDataset(X_tr, y_tr) if aug else NoAugDataset(X_tr, y_tr)
    vl_ds = NoAugDataset(X_vl, y_vl)
    tr_ld = DataLoader(tr_ds, batch_size=BATCH_AB, shuffle=True, drop_last=True)
    vl_ld = DataLoader(vl_ds, batch_size=BATCH_AB, shuffle=False)
    m   = model_fn().to(device)
    opt = torch.optim.AdamW(m.parameters(), lr=1e-3, weight_decay=1e-3)
    sch = torch.optim.lr_scheduler.CosineAnnealingLR(opt, T_max=EPOCHS_AB)
    crit= nn.CrossEntropyLoss(label_smoothing=0.1)
    for ep in range(EPOCHS_AB):
        m.train()
        for xb, yb in tr_ld:
            xb,yb = xb.to(device), yb.to(device)
            opt.zero_grad(); crit(m(xb),yb).backward(); opt.step()
        sch.step()
    m.eval()
    preds, trues = [], []
    with torch.no_grad():
        for xb, yb in vl_ld:
            preds.extend(m(xb.to(device)).argmax(1).cpu().numpy())
            trues.extend(yb.numpy())
    return f1_score(trues, preds, average="macro", zero_division=0)

