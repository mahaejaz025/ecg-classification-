# ── Shared SE block ───────────────────────────────────────────────────────────
class SEBlock1D_ab(nn.Module):
    def __init__(self, ch, r=8):
        super().__init__()
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc   = nn.Sequential(
            nn.Linear(ch,max(ch//r,4),bias=False), nn.ReLU(True),
            nn.Linear(max(ch//r,4),ch,bias=False), nn.Sigmoid())
    def forward(self, x):
        b,c,_ = x.shape
        return x * self.fc(self.pool(x).view(b,c)).view(b,c,1)

# ── Variant 2: No SE block ────────────────────────────────────────────────────
class MultiScaleECGCNN_noSE(nn.Module):
    def __init__(self, num_classes=5, dropout=0.5):
        super().__init__()
        ch = 48
        def _p(k):
            return nn.Sequential(
                nn.Conv1d(1,ch,k,padding=k//2,bias=False),nn.BatchNorm1d(ch),nn.ReLU(True),
                nn.Dropout(dropout*0.5),
                nn.Conv1d(ch,ch,k,padding=k//2,bias=False),nn.BatchNorm1d(ch),nn.ReLU(True),
                nn.AdaptiveAvgPool1d(32))
        self.path_small  = _p(3)
        self.path_medium = _p(9)
        self.path_large  = _p(21)
        mc = ch*3
        # NO SE block here
        self.fusion     = nn.Sequential(
            nn.Dropout(dropout*0.6), nn.Conv1d(mc,96,3,padding=1,bias=False),
            nn.BatchNorm1d(96), nn.ReLU(True), nn.AdaptiveAvgPool1d(1))
        self.classifier = nn.Sequential(
            nn.Dropout(dropout), nn.Linear(96,64), nn.ReLU(True),
            nn.Dropout(dropout*0.5), nn.Linear(64,num_classes))
    def forward(self, x):
        if x.dim()==2: x=x.unsqueeze(1)
        f = torch.cat([self.path_small(x),self.path_medium(x),self.path_large(x)],1)
        return self.classifier(self.fusion(f).squeeze(-1))

# ── Variant 3: Single-scale only (kernel=9 only) ─────────────────────────────
class SingleScaleECGCNN(nn.Module):
    def __init__(self, num_classes=5, dropout=0.5):
        super().__init__()
        ch = 48*3   # same param budget as 3 paths
        self.features = nn.Sequential(
            nn.Conv1d(1,ch,9,padding=4,bias=False), nn.BatchNorm1d(ch), nn.ReLU(True),
            nn.Dropout(dropout*0.5),
            nn.Conv1d(ch,ch,9,padding=4,bias=False), nn.BatchNorm1d(ch), nn.ReLU(True),
            nn.AdaptiveAvgPool1d(32))
        mc = ch
        self.se         = SEBlock1D_ab(mc)
        self.fusion     = nn.Sequential(
            nn.Dropout(dropout*0.6), nn.Conv1d(mc,96,3,padding=1,bias=False),
            nn.BatchNorm1d(96), nn.ReLU(True), nn.AdaptiveAvgPool1d(1))
        self.classifier = nn.Sequential(
            nn.Dropout(dropout), nn.Linear(96,64), nn.ReLU(True),
            nn.Dropout(dropout*0.5), nn.Linear(64,num_classes))
    def forward(self, x):
        if x.dim()==2: x=x.unsqueeze(1)
        return self.classifier(self.fusion(self.se(self.features(x))).squeeze(-1))

# ── Variants 4 & 5: Full model (with / without augmentation) ─────────────────
class MultiScaleECGCNN_v2_ab(nn.Module):
    def __init__(self, num_classes=5, dropout=0.5):
        super().__init__()
        ch = 48
        def _p(k):
            return nn.Sequential(
                nn.Conv1d(1,ch,k,padding=k//2,bias=False),nn.BatchNorm1d(ch),nn.ReLU(True),
                nn.Dropout(dropout*0.5),
                nn.Conv1d(ch,ch,k,padding=k//2,bias=False),nn.BatchNorm1d(ch),nn.ReLU(True),
                nn.AdaptiveAvgPool1d(32))
        self.path_small  = _p(3)
        self.path_medium = _p(9)
        self.path_large  = _p(21)
        mc = ch*3
        self.se         = SEBlock1D_ab(mc)
        self.fusion     = nn.Sequential(
            nn.Dropout(dropout*0.6),nn.Conv1d(mc,96,3,padding=1,bias=False),
            nn.BatchNorm1d(96),nn.ReLU(True),nn.AdaptiveAvgPool1d(1))
        self.classifier = nn.Sequential(
            nn.Dropout(dropout),nn.Linear(96,64),nn.ReLU(True),
            nn.Dropout(dropout*0.5),nn.Linear(64,num_classes))
    def forward(self, x):
        if x.dim()==2: x=x.unsqueeze(1)
        f = torch.cat([self.path_small(x),self.path_medium(x),self.path_large(x)],1)
        return self.classifier(self.fusion(self.se(f)).squeeze(-1))

