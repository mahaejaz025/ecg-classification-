def check_incart_complete(d, expected_records=75):
    if not os.path.isdir(d):
        return False, [], 0
    files = os.listdir(d)
    hea_files = sorted({f.split('.')[0] for f in files if f.endswith('.hea')})
    missing = []
    for rec in hea_files:
        for ext in ('.hea', '.dat', '.atr'):
            if not os.path.exists(os.path.join(d, f"{rec}{ext}")):
                missing.append(f"{rec}{ext}")
    complete = (len(hea_files) == expected_records) and (len(missing) == 0)
    return complete, missing, len(hea_files)

