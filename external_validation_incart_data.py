import torch, torch.nn as nn, torch.nn.functional as F, os

# --- Architecture (must match training-time definition exactly) ---
class SqueezeExcitation1D(nn.Module):
    def __init__(self, channels, reduction=16):
        super().__init__()
        self.fc = nn.Sequential(
            nn.Linear(channels, channels // reduction, bias=False), nn.ReLU(True),
            nn.Linear(channels // reduction, channels, bias=False), nn.Sigmoid())
    def forward(self, x):
        b, c, _ = x.size(); y = x.mean(dim=2)
        return x * self.fc(y).view(b, c, 1).expand_as(x)

class ResNetBlock1D(nn.Module):
    def __init__(self, in_ch, out_ch, stride=1):
        super().__init__()
        self.conv1 = nn.Conv1d(in_ch, out_ch, 3, stride=stride, padding=1, bias=False)
        self.bn1   = nn.BatchNorm1d(out_ch)
        self.conv2 = nn.Conv1d(out_ch, out_ch, 3, padding=1, bias=False)
        self.bn2   = nn.BatchNorm1d(out_ch)
        self.se    = SqueezeExcitation1D(out_ch)
        self.skip  = nn.Sequential(nn.Conv1d(in_ch, out_ch, 1, stride=stride, bias=False),
                                    nn.BatchNorm1d(out_ch)) if in_ch != out_ch or stride != 1 else nn.Identity()
    def forward(self, x):
        return F.relu(self.se(F.relu(self.bn2(self.conv2(F.relu(self.bn1(self.conv1(x))))))) + self.skip(x))

class ECGResNetSE(nn.Module):
    def __init__(self, num_classes=5):
        super().__init__()
        self.stem   = nn.Sequential(nn.Conv1d(1, 64, 7, padding=3, bias=False), nn.BatchNorm1d(64))
        self.layer1 = ResNetBlock1D(64, 128, stride=2)
        self.layer2 = ResNetBlock1D(128, 256, stride=2)
        self.pool   = nn.AdaptiveAvgPool1d(1)
        self.fc     = nn.Linear(256, num_classes)
    def forward(self, x):
        if x.dim() == 2: x = x.unsqueeze(1)
        return self.fc(self.pool(self.layer2(self.layer1(F.relu(self.stem(x))))).squeeze(-1))

