import os
import torch
import torch.nn as nn

class DropPath(nn.Module):
    def __init__(self, drop_prob=0.15):
        super().__init__()
        self.drop_prob = drop_prob
    def forward(self, x):
        if not self.training or self.drop_prob == 0.0:
            return x
        keep = 1.0 - self.drop_prob
        shape = (x.shape[0],) + (1,) * (x.ndim - 1)
        mask = torch.rand(shape, device=x.device) < keep
        return x * mask.float() / keep

class SEBlock1D(nn.Module):
    def __init__(self, channels, reduction=8):
        super().__init__()
        self.pool = nn.AdaptiveAvgPool1d(1)
        self.fc = nn.Sequential(
            nn.Linear(channels, max(channels // reduction, 4), bias=False),
            nn.ReLU(inplace=False),
            nn.Linear(max(channels // reduction, 4), channels, bias=False),
            nn.Sigmoid()
        )
    def forward(self, x):
        b, c, _ = x.shape
        y = self.pool(x).view(b, c)
        return x * self.fc(y).view(b, c, 1)

class MultiScaleECGCNN_v2(nn.Module):
    def __init__(self, num_classes=5, input_len=187, dropout=0.5, drop_path=0.15):
        super().__init__()
        ch = 48
        def _path(k):
            pad = k // 2
            return nn.Sequential(
                nn.Conv1d(1, ch, kernel_size=k, padding=pad, bias=False),
                nn.BatchNorm1d(ch), nn.ReLU(inplace=False), nn.Dropout(dropout * 0.5),
                nn.Conv1d(ch, ch, kernel_size=k, padding=pad, bias=False),
                nn.BatchNorm1d(ch), nn.ReLU(inplace=False), nn.AdaptiveAvgPool1d(32)
            )
        self.path_small  = _path(3)
        self.path_medium = _path(9)
        self.path_large  = _path(21)
        self.dp_small  = DropPath(drop_path)
        self.dp_medium = DropPath(drop_path)
        self.dp_large  = DropPath(drop_path)
        merged_ch = ch * 3
        self.se = SEBlock1D(merged_ch)
        self.fusion = nn.Sequential(
            nn.Dropout(dropout * 0.6),
            nn.Conv1d(merged_ch, 96, kernel_size=3, padding=1, bias=False),
            nn.BatchNorm1d(96), nn.ReLU(inplace=False), nn.AdaptiveAvgPool1d(1)
        )
        self.classifier = nn.Sequential(
            nn.Dropout(dropout), nn.Linear(96, 64), nn.ReLU(inplace=False),
            nn.Dropout(dropout * 0.5), nn.Linear(64, num_classes)
        )
    def forward(self, x):
        if x.dim() == 2: x = x.unsqueeze(1)
        p1 = self.dp_small(self.path_small(x))
        p2 = self.dp_medium(self.path_medium(x))
        p3 = self.dp_large(self.path_large(x))
        fused = self.se(torch.cat([p1, p2, p3], dim=1))
        out = self.fusion(fused).squeeze(-1)
        return self.classifier(out)

